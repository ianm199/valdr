//! Build-time release metadata for the Valkey/Redis-compatible server.
//!
//! Corresponds to `release.c` and `release.h` in the Valkey C source.
//! Constants from `version.h` (which `release.h` `#include`s) are defined
//! canonically in `crate::version` and re-exported here, per the
//! PORTING.md rule that headers fold into the `.rs` that uses them — except
//! where `file-deps.tsv` assigns a header its own target file.
//!
//! In production, `REDIS_GIT_SHA1`, `REDIS_GIT_DIRTY`, and `REDIS_BUILD_ID`
//! would be injected by `build.rs` (querying `git describe`).  Phase A
//! hard-codes the values that were baked into `release.h` at snapshot time.
//!
//! # Phase A note
//! This file does not need to compile.  Name-resolution errors against
//! `crate::version` (not yet declared in `lib.rs`) are expected.

// PORT NOTE: In the C build system, release.h is regenerated by the Makefile
// on every git commit that changes SHA1 or dirty status.  In Rust the
// idiomatic replacement is a build.rs that writes constants into
// $OUT_DIR/release.rs and includes them via include!().  Phase B should
// introduce that mechanism and remove the hard-coded literals below.

